function comb = combine_all_testset_predictions(results, predictions)
    vars = ["subject", "WindowStart", "WindowSize", "filters", ...
            "target_label", "target_type", "sim_source", "sim_metric", ...
            "FiltersToApplyBeforeEmbedding", "RandomSeed"];
    vars = vars(ismember(vars, results.Properties.VariableNames));
    params = varfun(@(x) unique(x, 'rows'), results, ...
                    'InputVariables', vars, ...
                    'OutputFormat', 'cell');

    vars = vars(~cellfun('isempty', params));
    params = params(~cellfun('isempty', params));

    comb = expand_cellvec2table(params, vars);

    [~, results.group] = select_matching_rows(results, comb);

    comb.Cz = rowfun(@combine_testset_predictions, results, ...
                     'InputVariables', [predictions, "testset"], ...
                     'GroupingVariables', "group", ...
                     'OutputFormat', "cell");
end
